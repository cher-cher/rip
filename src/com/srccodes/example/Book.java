package com.srccodes.example;

public class Book {
	private String name;
	private String author;
	private int year;
	
	public String getName() { 
	    return this.name;
	}

	public void setName(String name) { 
	    this.name=name;
	}
	
	public String getAuthor() { 
	    return this.author;
	}

	public void setAuthor(String author) { 
	    this.author=author;
	}
	
	public int getYear() { 
	    return this.year;
	}

	public void setYear(int year) { 
	    this.year=year;
	}
}
